	public int largestRectangleArea(int[] heights) {
		if (heights == null || heights.length == 0) {
			return 0;
		}
		int i = 1, n = heights.length;
		LinkedList<Integer> stack = new LinkedList<>();
		stack.push(0);
		int curMax = 0;
		while (i < n) {
			int t = heights[i];
			if (stack.isEmpty() || t >= heights[stack.peek()]) {
				stack.push(i++);
			}
			else {
				int top = stack.pop();
				int topArea = heights[top] * (stack.isEmpty() ? i : i - 1 - stack.peek());
				curMax = Math.max(curMax, topArea);
			}
		}
		while (!stack.isEmpty()) {
			int top = stack.pop();
			int topArea = heights[top] * (stack.isEmpty() ? i : i - stack.peek() - 1);
			curMax = Math.max(curMax, topArea);
		}
		return curMax;
	}

	private int largestRectangleArea(int[] heights, int start, int end) {
		if (start > end) {
			return 0;
		}
		else if (start == end) {
			return heights[start];
		}
		List<Integer> mins = findMinId(heights, start, end);
		int s1 = start, s2 = mins.get(0), i = 1, n = mins.size();
		int rangeMax = largestRectangleArea(heights, s1, s2 - 1);
		while (i < n) {
			s1 = s2 + 1;
			s2 = mins.get(i++);
			int tmp = largestRectangleArea(heights, s1, s2 - 1);
			rangeMax = Math.max(rangeMax, tmp);
		}
		int tmp = largestRectangleArea(heights, s2 + 1, end);
		rangeMax = Math.max(rangeMax, tmp);
		int maxMin = heights[mins.get(0)] * (end - start + 1);
		return Math.max(rangeMax, maxMin);
	}

	private List<Integer> findMinId(int[] heights, int start, int end) {
		List<Integer> result = new ArrayList<>();
		int min = Integer.MAX_VALUE;
		for (int i = start; i <= end; i++) {
			if (heights[i] < min) {
				result.clear();
				result.add(i);
				min = heights[i];
			}
			else if (heights[i] == min) {
				result.add(i);
			}
		}
		return result;
	}

	public int largestRectangleArea1(int[] heights) {
		if (heights == null || heights.length == 0) {
			return 0;
		}
		int n = heights.length;
		int[] area = new int[n], left = new int[n], right = new int[n];
		left[0] = 0;
		right[n - 1] = 0;
		for (int i = 1; i < n; i++) {
			int count = 0;
			for (int j = i - 1; j >= 0; j--) {
				if (heights[j] == heights[i]) {
					count += (left[j] + 1);
					break;
				}
				else if (heights[j] > heights[i]) {
					count++;
				}
				else {
					break;
				}
			}
			left[i] = count;
		}
		for (int i = n - 2; i >= 0; i--) {
			int count = 0;
			for (int j = i + 1; j < n; j++) {
				if (heights[j] == heights[i]) {
					count += (right[j] + 1);
					break;
				}
				if (heights[j] > heights[i]) {
					count++;
				}
				else {
					break;
				}
			}
			right[i] = count;
		}
		int result = 0;
		for (int i = 0; i < n; i++) {
			area[i] = heights[i] * (left[i] + right[i] + 1);
			if (area[i] > result) {
				result = area[i];
			}
		}
		return result;
	}
